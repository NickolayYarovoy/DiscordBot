import numpy as np

def cos(num):
    return np.cos(np.radians(num))
 
 
def sin(num):
    return np.sin(np.radians(num))
 
 
def tg(num):
    return np.tan(np.radians(num))
 
 
def ctg(num):
    return 1 / np.tan(np.radians(num))
 
 
def ln(num):
    return np.log(np.radians(num))





